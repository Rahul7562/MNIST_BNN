module tb;
 
// ?????????????????????????????????????????????????????????????????????????????
//  Parameters
// ?????????????????????????????????????????????????????????????????????????????
parameter integer FOCUS_DIGIT = 0;     // ? change to 0-9 to test a different digit
parameter integer CLK_HALF    = 5;     // half-period in ns ? 10 ns = 100 MHz
parameter integer TIMEOUT     = 2000;  // max cycles before timeout error
 
// ?????????????????????????????????????????????????????????????????????????????
//  DUT ports
// ?????????????????????????????????????????????????????????????????????????????
reg          clk;
reg          rst_n;
reg          start;
reg  [783:0] image_in;
wire [3:0]   digit_out;
wire         valid;
 
// ?????????????????????????????????????????????????????????????????????????????
//  DUT instantiation
// ?????????????????????????????????????????????????????????????????????????????
    top dut (
    .clk       (clk),
    .rst_n     (rst_n),
    .start     (start),
    .image_in  (image_in),
    .digit_out (digit_out),
    .valid     (valid)
);
 
// ?????????????????????????????????????????????????????????????????????????????
//  Waveform probe wires - tap DUT internal signals for GTKWave
// ?????????????????????????????????????????????????????????????????????????????
wire [2:0]  dut_state      = dut.state;
wire [9:0]  dut_neuron_idx = dut.neuron_idx;
wire [3:0]  argmax_idx     = dut.argmax_idx;   // argmax workspace reg
 
// Individual output accumulators (visible as named channels in GTKWave)
wire signed [24:0] acc0 = dut.out_acc[0];
wire signed [24:0] acc1 = dut.out_acc[1];
wire signed [24:0] acc2 = dut.out_acc[2];
wire signed [24:0] acc3 = dut.out_acc[3];
wire signed [24:0] acc4 = dut.out_acc[4];
wire signed [24:0] acc5 = dut.out_acc[5];
wire signed [24:0] acc6 = dut.out_acc[6];
wire signed [24:0] acc7 = dut.out_acc[7];
wire signed [24:0] acc8 = dut.out_acc[8];
wire signed [24:0] acc9 = dut.out_acc[9];
 
// Popcount counter  (bit-count of hidden layer 1 - nice to see in waveform)
// Count of '1' bits in hidden1, updated each cycle of LAYER2
wire [9:0] h1_ones;
assign h1_ones = dut.hidden1[0] + dut.hidden1[1] + dut.hidden1[2] + dut.hidden1[3];
// (partial sum of first 4 bits shown as a lightweight indicator)
 
// ?????????????????????????????????????????????????????????????????????????????
//  Clock generation
// ?????????????????????????????????????????????????????????????????????????????
initial clk = 1'b1;
always #(CLK_HALF) clk = ~clk;
 
// ?????????????????????????????????????????????????????????????????????????????
//  Image storage - one 784-bit entry per digit
// ?????????????????????????????????????????????????????????????????????????????
reg [783:0] test_images [0:9];
reg [783:0] img_buf     [0:0];   // single-entry read buffer for $readmemb
 
task load_all_images;
    begin                                        // task body begin [T1
        $readmemb("mem_files/test_image_0.mem", img_buf); test_images[0] = img_buf[0];
        $readmemb("mem_files/test_image_1.mem", img_buf); test_images[1] = img_buf[0];
        $readmemb("mem_files/test_image_2.mem", img_buf); test_images[2] = img_buf[0];
        $readmemb("mem_files/test_image_3.mem", img_buf); test_images[3] = img_buf[0];
        $readmemb("mem_files/test_image_4.mem", img_buf); test_images[4] = img_buf[0];
        $readmemb("mem_files/test_image_5.mem", img_buf); test_images[5] = img_buf[0];
        $readmemb("mem_files/test_image_6.mem", img_buf); test_images[6] = img_buf[0];
        $readmemb("mem_files/test_image_7.mem", img_buf); test_images[7] = img_buf[0];
        $readmemb("mem_files/test_image_8.mem", img_buf); test_images[8] = img_buf[0];
        $readmemb("mem_files/test_image_9.mem", img_buf); test_images[9] = img_buf[0];
    end                                          // task body end T1]
endtask
 
// ?????????????????????????????????????????????????????????????????????????????
//  Task: drive one inference and return the result
// ?????????????????????????????????????????????????????????????????????????????
reg [3:0] infer_result;
reg       infer_pass;
integer   infer_cycles;
 
task run_inference;
    input [783:0] img;
    input integer  expected;
    integer tc;
    begin                                        // task body begin [T2
        image_in = img;
        @(posedge clk); #1;
        start = 1'b1;
        @(posedge clk); #1;
        start    = 1'b0;
        tc       = 0;
 
        while (!valid && tc < TIMEOUT) begin     // while body begin [T2a
            @(posedge clk); #1;
            tc = tc + 1;
        end                                      // while body end T2a]
 
        if (tc >= TIMEOUT) begin                 // timeout if begin [T2b
            $display("  [TIMEOUT] Digit %0d stalled after %0d cycles", expected, TIMEOUT);
            infer_result = 4'hF;
            infer_pass   = 1'b0;
        end else begin                           // T2b] else begin [T2c
            infer_result = digit_out;
            infer_pass   = (digit_out == expected[3:0]);
            infer_cycles = tc;
        end                                      // T2c]
 
        repeat(5) @(posedge clk); #1;   // hold image stable for waveform readability
    end                                          // task body end T2]
endtask
 
// ?????????????????????????????????????????????????????????????????????????????
//  Continuous monitor - prints every valid pulse to console
// ?????????????????????????????????????????????????????????????????????????????
always @(posedge clk) begin                      // monitor always begin [M1
    if (valid)
        $display("  [%7t ns]  valid=1  digit_out=4'b%04b  (%0d)",
                 $time, digit_out, digit_out);
end                                              // monitor always end M1]
 
// ?????????????????????????????????????????????????????????????????????????????
//  Main stimulus
// ?????????????????????????????????????????????????????????????????????????????
integer d;
integer pass_cnt;
integer fail_cnt;
 
initial begin                                    // stimulus begin [S1
 
    // ?? Waveform dump ??????????????????????????????????????????????????????
    $dumpfile("bnn_wave.vcd");
    $dumpvars(0, tb);   // dump everything: tb + DUT internals
 
    // ?? Initialise ?????????????????????????????????????????????????????????
    rst_n    = 1'b0;
    start    = 1'b0;
    image_in = 784'b0;
 
    load_all_images();
 
    // Reset for 4 cycles then release
    repeat(4) @(posedge clk); #1;
    rst_n = 1'b1;
    repeat(2) @(posedge clk); #1;
 
    $display("");
    $display("=================================================================");
    $display("  BNN Inference Testbench");
    $display("  Architecture : 784 -> 512 -> 256 -> 10 -> argmax -> 4-bit");
    $display("  Clock        : 100 MHz  |  Latency target : ~782 cycles");
    $display("=================================================================");
 
    // ?? PHASE 1: scan all 10 digits ????????????????????????????????????????
    $display("");
    $display("  Phase 1 - All digits");
    $display("  %s", "  Dig  Expected  Got   #  Cycles  Status");
    $display("  %s", "  ---  --------  ---  --  ------  ------");
 
    pass_cnt = 0;
    fail_cnt = 0;
 
    for (d = 0; d < 10; d = d + 1) begin        // scan loop begin [S2
        run_inference(test_images[d], d);
 
        if (infer_pass) begin                    // result if begin [S3
            pass_cnt = pass_cnt + 1;
            $display("  %3d  %04b      %04b   %1d  %4d    PASS",
                     d, d[3:0], infer_result, infer_result, infer_cycles);
        end else begin                           // S3] else begin [S4
            fail_cnt = fail_cnt + 1;
            $display("  %3d  %04b      %04b   %1d  %4d    FAIL ? expected %0d got %0d",
                     d, d[3:0], infer_result, infer_result, infer_cycles, d, infer_result);
        end                                      // S4]
    end                                          // scan loop end S2]
 
    $display("  ---  --------  ---  --  ------  ------");
    $display("  Score: %0d / 10 correct", pass_cnt);
 
    // ?? PHASE 2: focused waveform test ?????????????????????????????????????
    $display("");
    $display("=================================================================");
    $display("  Phase 2 - Focused waveform: digit %0d", FOCUS_DIGIT);
    $display("=================================================================");
 
    // Idle gap - makes the start of phase 2 obvious in GTKWave
    repeat(20) @(posedge clk); #1;
 
    // Fresh reset
    rst_n = 1'b0;
    repeat(4) @(posedge clk); #1;
    rst_n = 1'b1;
    repeat(4) @(posedge clk); #1;
 
    image_in = test_images[FOCUS_DIGIT];
    $display("  [%7t ns]  Image loaded for digit %0d", $time, FOCUS_DIGIT);
 
    // Start inference
    @(posedge clk); #1;
    start = 1'b1;
    $display("  [%7t ns]  start=1 - inference begins", $time);
    @(posedge clk); #1;
    start = 1'b0;
 
    // Wait, logging each state transition
    begin : watch_block                          // named block begin [W1
        reg [2:0] prev_st;
        integer   watch_tc;
        prev_st  = dut_state;
        watch_tc = 0;
 
        while (!valid && watch_tc < TIMEOUT) begin   // watch while begin [W2
            @(posedge clk); #1;
            watch_tc = watch_tc + 1;
 
            if (dut_state !== prev_st) begin         // state change begin [W3
                case (dut_state)
                    3'd1: $display("  [%7t ns]  ? LAYER1  (512 BNN neurons)", $time);
                    3'd2: $display("  [%7t ns]  ? LAYER2  (256 BNN neurons)", $time);
                    3'd3: $display("  [%7t ns]  ? OUTPUT  (10 Q8.8 accumulators)", $time);
                    3'd4: $display("  [%7t ns]  ? ARGMAX  (1 cycle, combinatorial)", $time);
                    3'd5: $display("  [%7t ns]  ? DONE    valid=1  digit_out=%04b (%0d)",
                                   $time, digit_out, digit_out);
                    default: ;
                endcase
                prev_st = dut_state;
            end                                      // state change end W3]
        end                                          // watch while end W2]
    end                                              // named block end W1]
 
    // Final report
    $display("");
    $display("  ?????????????????????????????????????????????????????????");
    $display("  Input digit    : %0d",         FOCUS_DIGIT);
    $display("  Expected bits  : %04b",         FOCUS_DIGIT[3:0]);
    $display("  digit_out      : %04b  (%0d)",  digit_out, digit_out);
    $display("  valid          : %b",           valid);
    $display("");
    $display("  Output accumulator scores (Q8.8 raw - larger = more confident):");
    $display("    class 0: %0d", $signed(acc0));
    $display("    class 1: %0d", $signed(acc1));
    $display("    class 2: %0d", $signed(acc2));
    $display("    class 3: %0d", $signed(acc3));
    $display("    class 4: %0d", $signed(acc4));
    $display("    class 5: %0d", $signed(acc5));
    $display("    class 6: %0d", $signed(acc6));
    $display("    class 7: %0d", $signed(acc7));
    $display("    class 8: %0d", $signed(acc8));
    $display("    class 9: %0d", $signed(acc9));
    $display("");
 
    if (digit_out == FOCUS_DIGIT[3:0]) begin         // final check begin [S5
        $display("  Result : *** PASS ***  digit %0d correctly classified", FOCUS_DIGIT);
    end else begin                                   // S5] else begin [S6
        $display("  Result : *** FAIL ***  expected %0d, got %0d",
                 FOCUS_DIGIT, digit_out);
    end                                              // S6]
 
    $display("  ?????????????????????????????????????????????????????????");
 
    // Hold stable so the final state is clearly visible in the waveform
    repeat(30) @(posedge clk); #1;
 
    $display("");
    $display("=================================================================");
    $display("  Simulation done. Open bnn_wave.vcd in GTKWave.");
    $display("  Suggested signal groups:");
    $display("    [Control]   clk  rst_n  start  valid  digit_out");
    $display("    [FSM]       dut_state  dut_neuron_idx");
    $display("    [Scores]    acc0 acc1 acc2 acc3 acc4 acc5 acc6 acc7 acc8 acc9");
    $display("    [Argmax]    argmax_idx");
    $display("=================================================================");
    $display("");
 
    $finish;
 
end                                              // stimulus end S1]
 
endmodule
